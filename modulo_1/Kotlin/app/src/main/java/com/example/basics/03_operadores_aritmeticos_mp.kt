package com.example.basics

fun main() {
    println("OPERADORES ARITMÉTICOS EN MI CEMENTERIO ️")

    val tumbasSectorA: Double = 25.8
    val tumbasSectorB: Double = 3.0

    println("Suma de tumbas (A + B): ${tumbasSectorA + tumbasSectorB}")
    println("Resta de tumbas (A - B): ${tumbasSectorA - tumbasSectorB}")
    println("Multiplicación (tumbas por flores por tumba): ${tumbasSectorA * tumbasSectorB}")
    println("División (promedio de tumbas por jardinero): ${tumbasSectorA / tumbasSectorB}")

    println("\nContexto:")
    println("En el Cementerio Jardines del Silencio, el cuidador calcula la distribución de tumbas por sector.")
    println("La suma indica el total de tumbas en dos sectores.")
    println("La resta muestra la diferencia entre ellos.")
    println("La multiplicación estima las flores que debe colocar si cada tumba tiene flores.")
    println("Y la división representa el promedio de tumbas atendidas por cada jardinero en su turno nocturno.")
}
